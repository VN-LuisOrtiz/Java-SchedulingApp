package Model;

public class City {
    private int cityID;
    private String city;

    //Default Constructor
    public City(){}

    //Getters and Setters
    public int getCityID(){
        return this.cityID;
    }

    public void setCityID(int cityID){
        this.cityID=cityID;
    }

    public String getCity(){
        return this.city;
    }

    public void setCity(String city){
        this.city=city;
    }



}
